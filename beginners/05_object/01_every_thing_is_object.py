'''
Everything is object in python
'''

if __name__ == "__main__":
    # Primitives are object
    # int is object
    print(type(10))

    # float is object
    print(type(10.5))

    # boolean is object
    print(type(True))

    # None is object
    print(type(None))

    # even modules are objects
    import sys

    print(type(sys))
