'''Operations on Floats'''
# addition
print(0.2 + 0.2)

# subtraction
print(0.2 - 0.2)

# Multiplication
print(2 * 0.1)

# Addition
print(0.2 + 0.1)  # The outcome will be 0.30000000000000004

# Python defaults to a float in any operation that uses a float, even if the output is a whole number.
print(0.2 + 0.8)

print(2 * 0.2)
