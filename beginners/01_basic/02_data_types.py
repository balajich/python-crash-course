# integer data type
# 42 bit arbitrary precision integer
# unlimited precision signed integer
print(type(10))

# float data type
# 64-bit floating point numbers
# 53 bits of binary precision
# 15 to 16 bits of decimal precision
print(type(10.5))

# String data type
print(type('Hello'))

# Bool data type
print(type(True))

# None data type
# the null object
print(type(None))
