'''
List is an unordered collection
'''

if __name__ == "__main__":
    # Create list
    l = [1, 2, 3]

    # access element by index
    # List index starts with 0
    print(l[0])

    # iterate through list
    for e in l:
        print(e)

    # size or number of elements in list
    print(len(l))
