'''
Slice operations on list
'''

if __name__ == "__main__":
    # Create list
    l = [1, 2, 3]
    print(l)  # print entire list

    print(l[:])  # Access all the elements
    print(l[0:3])  # first to last
    print(l[:-1])  # first to last but one
    print(l[1:])  # second element to last
    print(l[-2:])  # last two elements
    print(l[-1:1])  # with out first and last
